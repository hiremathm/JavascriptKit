const n1 = 10;
const n2 = 17.5;
const n3 = '25';
const n4 = '25.75'
// Number to string
console.log(String(n1)) // '10'
console.log(String(n2)) // '17.5'
console.log("" + n1) // 10

// String to number
console.log(parseInt(n3)) // 25
console.log(parseInt(n4)) // 25
console.log(parseFloat(n3)) // 25.0
console.log(parseFloat(n4)) // 25.75

console.log(Number(n3)) // 25
console.log(Number(n4)) // 25.75



